public class PagoInscripcion {
    
    private int k_pago; 
    private String o_formapago;
    private String f_pago ;
    private int v_pago;
    private int k_ins;

    public int getK_pago() {
        return k_pago;
    }

    public void setK_pago(int k_pago) {
        this.k_pago = k_pago;
    }

    public String getO_formapago() {
        return o_formapago;
    }

    public void setO_formapago(String o_formapago) {
        this.o_formapago = o_formapago;
    }

    public String getF_pago() {
        return f_pago;
    }

    public void setF_pago(String f_pago) {
        this.f_pago = f_pago;
    }

    public int getV_pago() {
        return v_pago;
    }

    public void setV_pago(int v_pago) {
        this.v_pago = v_pago;
    }

    public int getK_ins() {
        return k_ins;
    }

    public void setK_ins(int k_ins) {
        this.k_ins = k_ins;
    }
    
}
